# 3.	По введенным пользователем координатам двух точек вывести
# уравнение прямой вида y = kx + b, проходящей через эти точки.
x1 = int(input('Введите x1\n'))
y1 = int(input('Введите y1\n'))
x2 = int(input('Введите x2\n'))
y2 = int(input('Введите y2\n'))
k = (y2 - y1) / (x2 - x1)
b = y1 - ((y2 - y1) / (x2 - x1)) * x1
if b >= 0:
    print(f'y = {k}x + {b}')
else:
    print(f'y = {k}x - {abs(b)}')
